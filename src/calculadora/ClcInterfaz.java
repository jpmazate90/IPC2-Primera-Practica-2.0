    public void operacion(){
        if(Texto.getText().equals("Ans")){
            resul = ans;
            Texto.setText(String.valueOf(resul));
        }
        if(Texto.getText().contains("+")){
        Double[] vector = new Double[Texto.getText().split("\\+").length];
            try{
                for (int i = 0; i < Texto.getText().split("\\+").length; i++) {
                    if(Texto.getText().split("\\+")[i].equals("Ans")){
                        vector[i]= ans;
                    }else{
                        vector[i] = Double.parseDouble(Texto.getText().split("\\+")[i]);
                    }
                }
                ans = basicas.operacionSuma(vector[0], vector[1]);
                if(vector.length>2){
                    for (int i = 2; i < vector.length; i++) {
                        ans = basicas.operacionSuma(ans,vector[i]);
                    }
                }
                resul=ans;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se ingresaron algunos valores vÃ¡lidos,o hay '+' seguidos.");
            }
        }
        if(Texto.getText().contains("-")){
        Double[] vector = new Double[Texto.getText().split(("\\-")).length];
            try{
                for (int i = 0; i < Texto.getText().split("\\-").length; i++) {
                    if(Texto.getText().split("\\-")[i].equals("Ans")){
                        vector[i]= ans;
                    }else{
                        vector[i] = Double.parseDouble(Texto.getText().split("\\-")[i]);
                    }
                }
                ans = basicas.operacionResta(vector[0], vector[1]);
                if(vector.length>2){
                    for (int i = 2; i < vector.length; i++) {
                        ans = basicas.operacionResta(ans,vector[i]);
                    }
                }
                resul=ans;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se ingresaron algunos valores vÃ¡lidos, o hay '-' seguidos.");
            }
        }
        if(Texto.getText().contains("*")){
        Double[] vector = new Double[Texto.getText().split(("\\*")).length];
            try{
                for (int i = 0; i < Texto.getText().split("\\*").length; i++) {
                    if(Texto.getText().split("\\*")[i].equals("Ans")){
                        vector[i]= ans;
                    }else{
                        vector[i] = Double.parseDouble(Texto.getText().split("\\*")[i]);
                    }
                }
                ans = basicas.operacionMultiplicacion(vector[0], vector[1]);
                if(vector.length>2){
                    for (int i = 2; i < vector.length; i++) {
                        ans = basicas.operacionMultiplicacion(ans,vector[i]);
                    }
                }
                resul=ans;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se ingresaron algunos valores vÃ¡lidos, o hay '*' seguidos.");
            }
        }
        if(Texto.getText().contains("/")){
        Double[] vector = new Double[Texto.getText().split(("\\/")).length];
            try{
                for (int i = 0; i < Texto.getText().split("\\/").length; i++) {
                    if(Texto.getText().split("\\/")[i].equals("Ans")){
                        vector[i]= ans;
                    }else{
                        vector[i] = Double.parseDouble(Texto.getText().split("\\/")[i]);
                    }
                }
                ans = basicas.operacionDivision(vector[0], vector[1]);
                if(vector.length>2){
                    for (int i = 2; i < vector.length; i++) {
                      ans = basicas.operacionDivision(ans,vector[i]);
                    }
                }
                resul=ans;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se ingresaron algunos valores vÃ¡lidos, o hay '/' seguidos.");
            }
        }
        if(Texto.getText().contains("%")){
        Double[] vector = new Double[Texto.getText().split(("\\%")).length];
            try{
                for (int i = 0; i < Texto.getText().split("\\%").length; i++) {
                    if(Texto.getText().split("\\%")[i].equals("Ans")){
                        vector[i]= ans;
                    }else{
                        vector[i] = Double.parseDouble(Texto.getText().split("\\%")[i]);
                    }
                }
                ans = basicas.operacionResiduo(vector[0], vector[1]);
                if(vector.length>2){
                    for (int i = 2; i < vector.length; i++) {
                      ans = basicas.operacionResiduo(ans,vector[i]);
                    }
                }
                resul=ans;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se ingresaron algunos valores vÃ¡lidos, o hay '%' seguidos.");
            }
        }
        if(Texto.getText().contains("^")){
            Double base, exponente;
            try{
                if(Texto.getText().substring(0, posicion).equals("Ans")){
                    base = ans;
                }else{
                    base = Double.parseDouble(Texto.getText().substring(0,posicion));
                }
                exponente = Double.parseDouble(Texto.getText().substring(posicion+1,Texto.getText().length()));
                resul = basicas.operacionPotencia(exponente, base);
                ans = resul;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se ingresaron algunos valores validos.");
            }
        }
        if(Texto.getText().contains("RaizDe")){
            Double exponente, base;
            try{
                if(Texto.getText().substring(0,posicion).equals("Ans")){
                    exponente = ans;
                }else{
                    exponente = Double.parseDouble(Texto.getText().substring(0,posicion));
                }
                base = Double.parseDouble(Texto.getText().substring(posicion+6,Texto.getText().length()));
                resul = basicas.operacionRaiz(exponente, base);
                ans = resul;
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "No se ingresaron algunos valores validos.");
            }
        }
    }
    
